const utf8Decoder = new TextDecoder("utf-8", { fatal: true });
const windows1251TrailByteByChar = createByteLookup(windows1251TrailBytes);
const windows1252TrailByteByChar = createByteLookup(windows1252TrailBytes);
const mojibakeRunPattern = createMojibakeRunPattern();

export function repairAdminReportText(value: string): string {
  return value.replace(mojibakeRunPattern, decodeMojibakeRun);
}

function createByteLookup(entries: readonly (readonly [number, number])[]) {
  return new Map(entries.map(([codePoint, byte]) => [String.fromCodePoint(codePoint), byte]));
}

function createMojibakeRunPattern() {
  const windows1251TrailClass = regexCharacterClass([...windows1251TrailByteByChar.keys()]);
  const windows1252TrailClass = regexCharacterClass([...windows1252TrailByteByChar.keys()]);

  return new RegExp(
    `(?:[\\u0420\\u0421][${windows1251TrailClass}])+|(?:[\\u00d0\\u00d1][${windows1252TrailClass}])+`,
    "g",
  );
}

function regexCharacterClass(characters: string[]) {
  return characters.map((character) => character.replace(/[\\\]\-\^]/g, "\\$&")).join("");
}

function decodeMojibakeRun(run: string): string {
  const bytes: number[] = [];

  for (let index = 0; index < run.length; index += 2) {
    const lead = run[index];
    const trail = run[index + 1];
    const leadByte = mojibakeLeadByte(lead);
    const trailByte = mojibakeTrailByte(lead, trail);

    if (leadByte === null || trailByte === undefined) return run;
    bytes.push(leadByte, trailByte);
  }

  try {
    const decoded = utf8Decoder.decode(new Uint8Array(bytes));
    return /[\u0400-\u04ff]/.test(decoded) ? decoded : run;
  } catch {
    return run;
  }
}

function mojibakeLeadByte(lead: string) {
  if (lead === "\u0420" || lead === "\u00d0") return 0xd0;
  if (lead === "\u0421" || lead === "\u00d1") return 0xd1;
  return null;
}

function mojibakeTrailByte(lead: string, trail: string) {
  if (lead === "\u0420" || lead === "\u0421") return windows1251TrailByteByChar.get(trail);
  return windows1252TrailByteByChar.get(trail);
}
